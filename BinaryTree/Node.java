public class Node {
    public Node left;
    public Node right;
    public String data;
    
    public Node getLeft() {
      return left;
    }
    public void setLeft(Node left) {
      this.left = left;
    }
    public Node getRight() {
      return right;
    }
    public void setRight(Node right) {
      this.right = right;
    }
    public String getData() {
      return data;
    }
    public void setData(String data) {
      this.data = data;
    }
  }
